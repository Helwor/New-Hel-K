local function SaveOriGround()
	setmetatable(oriGround, {__index = function(self, col) local row = {} rawset(self, col, row) return row end })
	local success = true
	for x = 0, groundCellsX - 1 do
		for y = 0, groundCellsY - 1 do
			local oriTex = gl.CreateTexture(bigTexSize, bigTexSize, fmt_groundGet)
			local _success = Spring.GetMapSquareTexture(x, y, 0, oriTex)
			if _success then
				oriGround[x][y] = oriTex
			else
				Echo('(SaveOriGround) FAILED sampling Ground Texture !')
			end
			success = success and _success
		end
	end
	Echo('Saving Original Ground ' .. (success and 'success' or 'FAILED!'))
	setmetatable(oriGround, nil)
end
local function RevertGround()

	if next(groundTexs) or uniTex then
		for tex in pairs(groundTexs) do
			gl.DeleteTexture(tex)
		end
		if uniTex then
			gl.DeleteTextureFBO(uniTex)
			gl.DeleteTexture(uniTex)
			uniTex = nil
		end
		for x = 0, groundCellsX - 1 do
			for y = 0, groundCellsY - 1 do
				Spring.SetMapSquareTexture(x, y, oriGround[x] and oriGround[x][y] or '')
				-- when restoring or if error, it report false in both case, we can't check for success in that case
			end
		end
		Echo('Reverted Ground Textures ')
	end
end

local function UniGround()
	local r, g, b = unpack(options['ground_color-'..mapName].value)
	if not uniTex then
		uniTex = gl.CreateTexture(1, 1, fmt_mipmap)
	end
	gl.RenderToTexture(uniTex, function()
		gl.Color(r, g, b, 1)
		gl.Rect(-1, -1, 1, 1)
	end)
	
	gl.GenerateMipmap(uniTex)
	local success = true
	for x = 0, groundCellsX - 1 do
		for y = 0, groundCellsY - 1 do
			success = Spring.SetMapSquareTexture(x, y, uniTex) and success
		end
	end
	Echo('Painting Ground ' .. (success and 'success' or 'FAILED!'))
end

local function ReduceGroundDefinition() -- this work but doesnt help FPS really
	RevertGround()
	local success = true
	for x = 0, groundCellsX - 1 do
		for y = 0, groundCellsY - 1 do
			local tmpTex = gl.CreateTexture(bigTexSize, bigTexSize, fmt_groundGet)
			-- local info = gl.TextureInfo(tmpTex)
			local _success = Spring.GetMapSquareTexture(x, y, 0, tmpTex)
			-- Echo("success is ", _success, 'tex info', info.id, info.xsize, info.ysize)
			if _success then
				local rtex = ReduceTexture(tmpTex, ground_scale, true)
				if rtex then
					Spring.SetMapSquareTexture(x, y, rtex)
					groundTexs[rtex] = true
				else
					_success = false
				end

			end
			success = success and _success
			gl.DeleteTexture(tmpTex)
		end
	end
	Echo('Reducing Ground Definition ' .. (ground_scale * 100)..'% '.. (success and 'success' or 'FAILED!'))
end


