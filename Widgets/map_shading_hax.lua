local function ReduceTexture(tex, scale, yReverse, format, noBlending, R,G,B,A) -- some textures, like splat_distr doesn't give the same result if copied at 100% size. format problem probably
	local info = gl.TextureInfo(tex)

	if not info or info.xsize == 0 or info.ysize == 0 then
		Echo('INVALID TEXTURE', tex, 'info', info, 'size', info and info.xsize, info and info.ysize)
		return nil
	end

	local newW = math.max(1, math.floor(info.xsize * scale))
	local newH = math.max(1, math.floor(info.ysize * scale))
	
	local newTex = gl.CreateTexture(newW, newH, format or fmt_mipmap)
	
	if not newTex then
		Spring.Echo("(ReduceTexture) TEXTURE " .. tex .. " CREATION FAILED")
		return nil
	end
	gl.RenderToTexture(newTex, function()
		gl.Clear(GL.COLOR_BUFFER_BIT, 0,0,0,0)
		if noBlending then
			gl.Blending(false)
		end
		gl.Texture(0, tex)
		if yReverse then
			gl.TexRect(-1, 1, 1, -1)
		else
			gl.TexRect(-1, -1, 1, 1)
		end
		gl.Texture(0, false)
		if noBlending then
			gl.Blending(GL.SRC_ALPHA, GL.ONE_MINUS_SRC_ALPHA)
		end
	end)
	gl.GenerateMipmap(newTex)
	gl.DeleteTextureFBO(newTex) 
	return newTex
end


