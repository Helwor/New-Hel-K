	if ground_def then
		if needSaveOriGround then
			SaveOriGround()
			needSaveOriGround = false
		end
		if ground_def == 'reduce' then
			ReduceGroundDefinition()
		elseif ground_def == 'unicolor' then
			UniGround()
		elseif ground_def == 'original' then
			RevertGround()
		end
		ground_def = false
		return
	end
	if next(updateSMF) then
		for _, namedTex in ipairs(mapShadingTextures) do
			local param = updateSMF[namedTex]
			if param then
				if param == 'reduce' then
					local scale = options[namedTex:gsub('$ssmf_', '') .. '-'..mapName..'_scale'].value
					ReduceShadingTexture(namedTex, scale)
				elseif param == 'disable' then
					DisableShadingTexture(namedTex)
				elseif param == 'original' then
					RevertShadingTexture(namedTex)
				end
				updateSMF[namedTex] = nil
				break
			end
		end
	end
end

function widget:Initialize()
	SetOptionCommands()
end

function widget:Shutdown()
	RevertAllMapShadings()
	RevertGround()

	for namedTex, tex in pairs(currentSMFs) do
		gl.DeleteTexture(rtex)
	end
	for x, col in pairs(oriGround) do
		for y, oriTex in pairs(col) do
			gl.DeleteTexture(oriTex)
		end
	end
	for tex in pairs(groundTexs) do
		gl.DeleteTexture(tex)
	end
	if uniTex then
		gl.DeleteTextureFBO(uniTex)
		gl.DeleteTexture(uniTex)
	end
	widgetHandler.actionHandler:RemoveAction(widget, 'smf')
	widgetHandler.actionHandler:RemoveAction(widget, 'groundtex')
end

