local function GetBaseColor(namedTex)
	return 0,0,0,0
end

local function GetNeuterColor(namedTex)
	if namedTex:find('normals') then
		return NEUTER_NORMAL_R, NEUTER_NORMAL_G, NEUTER_NORMAL_B, NEUTER_NORMAL_A
	elseif namedTex:find('detail') then
		return NEUTER_DETAIL_R, NEUTER_DETAIL_G, NEUTER_DETAIL_B, NEUTER_DETAIL_A
	elseif namedTex:find('sky_refl') then
		return NEUTER_SKYREFL_R, NEUTER_SKYREFL_G, NEUTER_SKYREFL_B, NEUTER_SKYREFL_A
	end
end

local function GetNoBlending(namedTex)
	if namedTex:find('specular') or namedTex:find('normals') or namedTex:find('distr') or namedTex:find('detail') or namedTex:find('sky_refl') then
		return true
	end
end

local function GetFormat(namedTex)
	if namedTex:find('splat_normals') or namedTex:find('detail') then
		return fmt_mipmapNormals
	elseif namedTex:find('ssmf_normals') then
		return fmt_mipmapNormalsBig
	end
	return fmt_mipmapSMF
end

local function ReduceShadingTexture(namedTex, scale, comment)
	comment = comment or ''
	local main, sub = namedTex:gsub(':?%d*',''), tonumber(namedTex:match(':(%d+)$'))
	if currentSMFs[namedTex] then
		gl.DeleteTexture(currentSMFs[namedTex])
		currentSMFs[namedTex] = nil
	end
	Spring.SetMapShadingTexture(main, '', sub) 
	local rtex = ReduceTexture(namedTex, scale, true, GetFormat(namedTex), GetNoBlending(namedTex), GetBaseColor(namedTex))
	if not rtex then
		Echo('Texturing Failed', namedTex)
	else
		gl.GenerateMipmap(rtex)
		Echo('Reduce Shading ' .. comment .. ' ' .. namedTex .. ' ' .. (scale * 100)..'%', Spring.SetMapShadingTexture(main, rtex, sub) and 'success' or 'FAILED')
		currentSMFs[namedTex] = rtex
	end
end

local function DisableShadingTexture(namedTex, comment)
	comment = comment or ''
	local main, sub = namedTex:gsub(':?%d*',''), tonumber(namedTex:match(':(%d+)$'))
	if currentSMFs[namedTex] then
		gl.DeleteTexture(currentSMFs[namedTex])
		currentSMFs[namedTex] = nil
	end
	-- Spring.SetMapShadingTexture(main, '', sub)
	local rtex = gl.CreateTexture(1, 1, fmt_disable)
	gl.RenderToTexture(rtex, function()
		gl.Clear(GL.COLOR_BUFFER_BIT, GetNeuterColor(namedTex))
	end)
	gl.DeleteTextureFBO(rtex)
	gl.GenerateMipmap(rtex)
	if not rtex then
		Echo('No Shading Texture', namedTex)
	else
		Echo('Disable Shading ' .. comment .. ' ' .. namedTex, Spring.SetMapShadingTexture(main, rtex, sub) and 'success' or 'FAILED!')
		currentSMFs[namedTex] = rtex
	end
end

local function RevertShadingTexture(namedTex, comment)
	if currentSMFs[namedTex] then
		gl.DeleteTexture(currentSMFs[namedTex])
		currentSMFs[namedTex] = nil
		comment = comment or ''
		local main, sub = namedTex:gsub(':?%d*',''), tonumber(namedTex:match(':(%d+)$'))
		Echo('Revert Shading ' .. comment .. ' ' .. namedTex, Spring.SetMapShadingTexture(main, '', sub) and 'success' or 'FAILED!')
	end
end

local function RevertAllMapShadings()
	for name_opt, opt in pairs(options) do
		if byName['$ssmf_'..name_opt:gsub('%-.*$', '')] then
			RevertShadingTexture('$ssmf_'..name_opt:gsub('%-.*$', ''))
		end
	end
end	




