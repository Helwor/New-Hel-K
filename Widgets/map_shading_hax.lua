local function SetOptionCommands()
	widgetHandler.actionHandler:AddAction(
		widget,
		'smf',
		function(cmd, paramStr, params)
			local param = params[1]
			if param == "options" then
				if WG.crude.OpenWidgetOptions then
					WG.crude.OpenWidgetOptions(widget)
				else
					WG.crude.OpenPath(options_path)
				end
			elseif param == 'reduce' then
				if params[2] then
					local scale = tonumber(params[2])
					if not scale then
						Echo('Wrong scale param '..tostring(params[2])..' for command /smf reduce [scale], value must be anumber (0-100)')
						return
					end
					scale = math.min(math.floor(scale + 0.5), 100) / 100
					local scaleOpt = options.smf_scale
					scaleOpt.value = scale
					scaleOpt:OnChange()
				end
				if options.smf_all.value ~= param then
					options.smf_all.value = param
					options.smf_all:OnChange()
				end
			elseif param == 'original' or param == 'disable' then
				options.smf_all.value = param
				options.smf_all:OnChange()
			else
				Echo('/smf unrecognized command param ' .. tostring(param) .. ', valids are "options", "original", "reduce", "disable"')
			end
		end,
		nil,
		't'
	)
	widgetHandler.actionHandler:AddAction(
		widget,
		'groundtex',
		function(cmd, paramStr, params) --
			if autoGenerated then
				Echo('/groundtex command unavailable with autogen maps')
				return
